export interface TwentyOneQuestionsState {
  phase: "playing" | "questionComplete" | "gameEnd";
  currentQuestionIndex: number;
  currentQuestion: string;
  questionIds: number[]; // IDs of selected questions (from the 100 question list)
  playerReady: {
    [playerId: string]: boolean; // Whether each player has clicked "next"
  };
  totalQuestions: number; // Total number of questions to ask (default 21)
  completedQuestions: number; // Number of questions completed
  players: string[]; // Player IDs
}

export interface TwentyOneQuestionsConfig {
  totalQuestions?: number; // Default 21
}

